  function get(name, opts) {
    const path = PATHS[name];
    if (!path) {
      console.warn("[icons] unknown:", name);
      return "";
    }
    const o = opts || {};
    const size = o.size || 16;
    const stroke = o.stroke || 1.75;
    const cls = o.class || "icon";
    return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${stroke}" stroke-linecap="round" stroke-linejoin="round" class="${cls}" aria-hidden="true">${path}</svg>`;
  }

  // 把整頁 [data-icon="name"] 的 element 內容換成 SVG。size 可用
  // [data-icon-size="20"] 覆寫，預設 16。
  function inject(root) {
    const r = root || document;
    r.querySelectorAll("[data-icon]").forEach((el) => {
      const name = el.getAttribute("data-icon");
      const size = parseInt(el.getAttribute("data-icon-size") || "16", 10);
      el.innerHTML = get(name, { size });
    });
  }

  window.Icons = { get, inject };
